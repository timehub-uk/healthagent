# ---------------------------------------------------------------------------
# Chunk splitting tests  (no external services needed)
# ---------------------------------------------------------------------------

class TestChunkSplitting:

    def test_split_even(self):
        lines = [f"line{i}" for i in range(100)]
        chunks = DNAChunkImporter._split_chunks(lines, 25)
        assert len(chunks) == 4
        assert all(len(c) == 25 for c in chunks)

    def test_split_remainder(self):
        lines = [f"line{i}" for i in range(103)]
        chunks = DNAChunkImporter._split_chunks(lines, 25)
        assert len(chunks) == 5          # 4 full + 1 remainder
        assert len(chunks[-1]) == 3

    def test_split_smaller_than_chunk(self):
        lines = ["a", "b", "c"]
        chunks = DNAChunkImporter._split_chunks(lines, 1000)
        assert len(chunks) == 1
        assert chunks[0] == ["a", "b", "c"]

    def test_split_empty(self):
        assert DNAChunkImporter._split_chunks([], 100) == []


